package com.itexps.sales;

/**
 *
 * @author polar
 */
public class Car {
    String vin;
    String color;
    String make;

    public String getVin() {
        return vin;
    }

    public void setVin(String vin) {
        this.vin = vin;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }    
    
    public String getMake() {
        return make;
    }

    public void setMake(String make) {
        this.make = make;
    }
    
}
